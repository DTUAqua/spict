        plot.col(inp$logmcovariatetime, inp$logmcovariate, do.line=FALSE, cex=0.6, add=TRUE, add.legend=FALSE)
        box(lwd=1.5)
    }
    txt.stamp(stamp, do.flag=TRUE)
}


