plotspict.growth <- function(rep, logax=FALSE, main='Time-varying growth', ylim=NULL,
                             xlim=NULL, xlab='Time', plot.ci=TRUE, stamp=get.version(), CI = 0.95){
    if (!'sderr' %in% names(rep) & rep$inp$timevaryinggrowth){
        log <- ifelse(logax, 'y', '')
        inp <- rep$inp
