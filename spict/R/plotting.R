            lines(inp$time[inp$indpred], BB[inp$indpred,3], col=cicol3, lty=1, lwd=1)
            abline(h=lineat)
            if ('man' %in% names(rep)){
                add.manlines(rep, 'logBBmsy', plot.legend=qlegend)
            }
            if (rep$opt$convergence != 0){
                warning.stamp()
            }
        } else {
            plot(1, typ='n', xlab='', ylab='', xaxt='n', yaxt='n', main=paste('Bmsy=NA!', main))
        }
        box(lwd=1.5)
        txt.stamp(stamp)
    }
